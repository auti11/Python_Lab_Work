mark = float(input("Enter your mark: "))

if mark >= 90:
    print("Grade: A+")
elif mark >= 85:
    print("Grade: A")
elif mark >= 80:
    print("Grade: B+")
elif mark >= 75:
    print("Grade: B")
elif mark >= 70:
    print("Grade: C+")
elif mark >= 65:
    print("Grade: C")
elif mark >= 60:
    print("Grade: D+")
elif mark >= 50:
    print("Grade: D")
else:
    print("Grade: F")